export default function getCollection(name) {
  switch (name) {
    case 'nocturnes':
      return {
        title: 'Nocturnes',
        collection: c2020
      }
 
    case 'StudioWork':
      return {
        title: 'Studio Work',
        collection: StudioWork
      }

    case 'EnPleinAir':
      return {
        title: 'En Plein Air',
        collection: EnPleinAir
      }
    case 'treelines':
      return {
        title: 'Treelines',
        collection: treelines
      }
    case 'all':
      return {
        title: 'all',
        collection: {
          art: EnPleinAir.art
            .concat(StudioWork.art)
            .concat(treelines.art)
        }
      }
    default:
      return {
        title: 'not found',
        collection: []
      }
  }
}
